<template>
	<div class="song-hot-search">
		<p class="hot-search-title">热搜榜</p>
		<ul>
			<li
				v-for="(item, index) in hotSearchList"
				:key="index"
				class="hot-search-list"
				@click="returnKey(item.searchWord)"
			>
				<span class="num">{{ index + 1 }}</span>
				<div class="song-info">
					<div class="song-info-title">
						<p class="song-name">{{ item.searchWord }}</p>
						<span class="num">{{ item.score }}</span>
						<img
							v-if="item.iconUrl"
							class="search-png"
							:src="item.iconUrl"
						/>
					</div>
					<!-- 歌曲介绍 -->
					<div class="song-Introduced">{{ item.content }}</div>
				</div>
			</li>
		</ul>
	</div>
</template>

<script>
import API from '@api';
export default {
	name: 'HotSearch',
	data() {
		return {
			hotSearchList: []
		};
	},
	created() {
		this.getList();
	},
	methods: {
		getList() {
			API.getHotSearchList()
				.then(res => {
					const data = res.data;
					if (data.code === 200) {
						this.hotSearchList = data.data;
					}
				})
				.catch(error => {
					console.log(error);
				});
		},
		returnKey(key) {
			this.$emit('returnKey', key);
		}
	}
};
</script>

